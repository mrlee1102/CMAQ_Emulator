void gridding(float ***input, float ****output, int batch_size, int region_num, int sector_num) {
    for (int i = 0; i < batch_size; i++)
    {
        for (int j = 0; j < region_num; j++)
        {
            for (int k = 0; k < sector_num; k++)
            {
                int grid_num = RegionCollection.gridInfo[j].gridNum;
                float value = input[i][j][k];
                for (int l = 0; l < grid_num; l++)
                {
                    int x = RegionCollection.gridInfo[j].gridIdx[l][1];
                    int y = RegionCollection.gridInfo[j].gridIdx[l][0];
                    float ratio = RegionCollection.gridInfo[j].gridRatio[l];
                    output[i][y][x][k] += value * ratio;
                }
            }
        }
    }
    return;
}

